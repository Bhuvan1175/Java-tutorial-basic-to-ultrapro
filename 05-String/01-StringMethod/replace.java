public class replace {
    public static void main(String[] args) {
        String str="Bhuvaneshwar Bagde";
        String newStr=str.replace("Bhuvaneshwar","Pratik");
        System.out.println("The original string is : "+str);
        System.out.println("After replacing 'Bhuvaneshwar' with 'Pratik', the string becomes: "+newStr);
    }
}
