public class charAt {
    public static void main(String[] args) {
        String str = "Bhuvaneshwar Bagde";
        for (int i = 0; i <str.length(); i++) {
            System.out.println("Char At ["+ i + "] =" + str.charAt(i));
        }
    }
}
