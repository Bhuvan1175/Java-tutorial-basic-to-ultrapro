public class logicalOperator {
    public static void main(String[] args) {
        int x = 10;
        int y=10;
        // AND Operator
        if(x==y && x<=10) System.out.println("x equal; to y");
        else System.out.println("x not equal or less than or equal to y");
        // OR Operator
        int a=2;
        int b=7;
        if(a>3 || b>8) System.out.println("TRUE");
        else System.out.println("FALSE");
    }    
}
