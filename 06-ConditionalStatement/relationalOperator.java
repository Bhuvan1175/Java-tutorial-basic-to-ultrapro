public class relationalOperator {
    public static void main(String[] args) {
        int x=7;
        int y=8;
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x==y);
        System.out.println(x!=y);
    }    
}
