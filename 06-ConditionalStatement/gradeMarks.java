public class gradeMarks {
    public static void main(String[] args) {
        int Marks=88;
        if(Marks>90){
            System.out.println("A+ Grade");
        }else if(Marks>80){
            System.out.println("A Grade");
        }else if(Marks > 70){
            System.out.println("B Grade");
        }else if(Marks > 60){
            System.out.println("C Grade");
        }else{
            System.out.println("F Grade");
        }
    }
}
