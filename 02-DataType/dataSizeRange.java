public class dataSizeRange {
    public static void main(String[] args) {
        System.out.println("Integer Data Type");
        System.out.println("Int Min :"+Integer.MIN_VALUE);
        System.out.println("Int Max :"+Integer.MAX_VALUE);
        System.out.println("Int Bytes :"+Integer.BYTES);

        System.out.println("Floating Data Type");
        System.out.println("Float Min :"+Float.MIN_VALUE);
        System.out.println("Float Max :"+Float.MAX_VALUE);
        System.out.println("Float Bytes :"+Float.BYTES);

        System.out.println("Bytes Data Type");
        System.out.println("Bytes Min :"+Byte.MIN_VALUE);
        System.out.println("Bytes Max :"+Byte.MAX_VALUE);
        System.out.println("Bytes Bytes :"+Byte.BYTES);
    }
}
