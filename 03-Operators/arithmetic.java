public class arithmetic {
    public static void main(String[] args) {
        int x=4;
        int y=6;
        int z=x+y;// Addition Operator
        int m=x*y;//Multiplication Operator
        int n=x-y;//Substraction Operator
        double o=x/y;//Division Operator
        int p=x%y;//Modulus Operator
        System.out.println("Addition : "+z);
        System.out.println("Multiplication : "+m);
        System.out.println("Subtraction : "+n);
        System.out.println("Division : "+o);
        System.out.println("Modulus : "+p);
    }
}
