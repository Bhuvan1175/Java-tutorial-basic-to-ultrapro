// There are 3 type of property.
// 1) Read and Writable
// 2) Read Only
// 3) Write Only
// 1) Read and Writable : The property shows both getter and setter methods. It
// means the value can be read as well write in this  property.
// 2) Read Only : the property shows only getter property , it means we cannot set
// any value to that particular property, but we can read that value.
//  3) Write Only: The property shows only setter method . We can assign values to
// that particular variable, but we cannot read that value.
