// Format Specifier
// Conversion
// Char c
//Integer d,o,x
//Floating f,e,g
// String S
public class conversionInt {
    public static void main(String arg[]) {
        int x = 10;
        System.out.printf("Using Decimal :%d \n", x);
        System.out.printf("Using Octale :%o \n", x);
        System.out.printf("Using Hexadecimal :%x \n", x);
    }
}